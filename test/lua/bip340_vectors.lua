local passed, failed = 0, 0

for _, v in ipairs(vectors) do
   local idx   = v[1]
   local sk    = v[2]
   local pk    = v[3]
   local aux   = v[4]
   local msg   = v[5]
   local sig   = v[6]
   local expect = v[7]
   local comment = v[8] or ""

   local sk_oct  = sk  and H(sk)  or nil
   local pk_oct  = pk  and H(pk)  or nil
   local aux_oct = aux and H(aux) or nil
   local msg_oct = H(msg)
   local sig_oct = H(sig)

   local desc = "vector " .. idx
   if comment ~= "" then desc = desc .. " (" .. comment .. ")" end

   local ok, err = pcall(function()
      -- Test pubgen when secret key is provided
      if sk_oct then
         local pk_computed = schnorr.pubgen(sk_oct)
         if pk_computed:hex():lower() ~= pk:lower() then
            error("pubgen mismatch: got " .. pk_computed:hex():lower() ..
                  " expected " .. pk:lower())
         end
      end

      -- Test sign when secret key is provided
      if sk_oct and msg_oct then
         local sig_computed = schnorr.sign(sk_oct, msg_oct, aux_oct)
         if sig_computed:hex():lower() ~= sig:lower() then
            error("sign mismatch: got " .. sig_computed:hex():lower() ..
                  " expected " .. sig:lower())
         end
      end

      -- Test verify for every row
      if pk_oct and msg_oct and sig_oct then
         local result = schnorr.verify(pk_oct, msg_oct, sig_oct)
         if result ~= expect then
            error("verify mismatch: got " .. tostring(result) ..
                  " expected " .. tostring(expect))
         end
      end
   end)

   if ok then
      passed = passed + 1
      io.write("OK  " .. desc .. "\n")
   else
      failed = failed + 1
      io.write("FAIL " .. desc .. ": " .. tostring(err) .. "\n")
   end
end

io.write(string.format("\nBIP-340 vectors: %d passed, %d failed (out of %d)\n",
                        passed, failed, #vectors))
if failed > 0 then
   error("BIP-340 vector tests FAILED")
end
