#include "algebra/nat.h"

#include "util/panic.h"

namespace proofs {
unsigned digit(char c) {
  if (c >= '0' && c <= '9') {
    return c - '0';
  } else if (c >= 'a' && c <= 'f') {
    return c - 'a' + 10;
  } else if (c >= 'A' && c <= 'F') {
    return c - 'A' + 10;
  } else {
    check(false, "malformed numeral in digit()");
    return 0;  // silence compiler warning
  }
}
}  // namespace proofs
